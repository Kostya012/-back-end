// Link1

let link1 = document.getElementById("link1");
if (link1) {
  link1.addEventListener(
    "click",
    function () {
      let list1 = document.getElementById("list1");
      if (list1.style.opacity === "") {
        list1.style.transform = "rotateX(0deg)";
        list1.style.visibility = "visible";
        list1.style.opacity = "1";
        list1.style.maxHeight = "1100px";
      } else if (list1.style.opacity === "0") {
        list1.style.transform = "rotateX(0deg)";
        list1.style.visibility = "visible";
        list1.style.opacity = "1";
        list1.style.maxHeight = "1100px";
      } else {
        list1.style.transform = "rotateX(-90deg)";
        list1.style.visibility = "hidden";
        list1.style.opacity = "0";
        list1.style.maxHeight = "0";
      }
    },
    true
  );
}

